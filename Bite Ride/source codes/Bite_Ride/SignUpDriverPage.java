    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
        
            try (Connection connection = DriverManager.getConnection(url, user, pass)) {
                String query = "INSERT INTO driver (phonenumber,vehicletype,time,firstname,middlename,lastname,age,sex,emailaddress,address,barangay,ssidnumber,licensetype,licensenumber,licenseexpiredate) VALUES (?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)";
                try (PreparedStatement statement = connection.prepareStatement(query)) {
                    statement.setString(1, txtPhoneNumber.getText());
                    statement.setString(2, cmbVehicleType.getSelectedItem().toString());
                    statement.setString(3, time);
                    statement.setString(4, txtFirstName.getText());
                    statement.setString(5, txtMiddleName.getText());
                    statement.setString(6, txtLastName.getText());
                    statement.setString(7, spinnerAge.getValue().toString());
                    statement.setString(8, sex);
                    statement.setString(9, txtEmailAddress.getText());
                    statement.setString(10, txtAddress.getText());
                    statement.setString(11, txtBarangay.getText());
                    statement.setString(12, txtSssIdNumber.getText());
                    statement.setString(13, licenseType);
                    statement.setString(14, txtDriverLicenceNumber.getText());
                    statement.setString(15, txtLicenseExpiryDate.getText());
                    
                    int rowsInserted = statement.executeUpdate();
                    
                    if (rowsInserted > 0) {
                        JOptionPane.showMessageDialog(null, "Thank You for Applying! Weâ€™ll get back to you shortly");
                        this.dispose();
                        
                        SettingsPage settingsPage = new SettingsPage();
                        settingsPage.SetAccount(phoneNumber, firstname, surname, password);
                        settingsPage.setVisible(true);
                        settingsPage.DisplayAccountInformation();
                    } else {
                        JOptionPane.showMessageDialog(null, "Failed to sign up");
                    }
                }
            }
        } catch (ClassNotFoundException | SQLException e) {
            JOptionPane.showMessageDialog(null, "Database error: " + e.getMessage());
            e.printStackTrace();
        }


    try {
        // 1. Source path from text field (user input)
        Path source = Paths.get(txtFile.getText());

        if (!Files.exists(source)) {
            JOptionPane.showMessageDialog(this,
                    "Source file not found.\nMake sure the image is accessible locally.",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        Path projectRoot = Paths.get(System.getProperty("user.dir"));

        Path destinationDir = projectRoot.resolve("Driver Photo File");
        Files.createDirectories(destinationDir);

        Path destination = destinationDir.resolve(source.getFileName());


        try (InputStream in = Files.newInputStream(source);
             OutputStream out = Files.newOutputStream(destination)) {

            in.transferTo(out);
        }

    } catch (IOException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this,
                "Unable to read the source file.\n" +
                "Cloud or phone storage must be downloaded first.",
                "Security Intake Error",
                JOptionPane.ERROR_MESSAGE);
    }


        
    }//GEN-LAST:event_btnSubmitActionPerformed

