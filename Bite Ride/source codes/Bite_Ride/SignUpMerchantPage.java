    private void btnSignUpMerchantActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignUpMerchantActionPerformed
        // TODO add your handling code here:
        
        try {
        Class.forName("com.mysql.cj.jdbc.Driver");

            try (Connection connection = DriverManager.getConnection(url, user, pass)) {
                String query = "INSERT INTO merchant (phonenumber,firstname,middlename,lastname,businessname,businessaddress,businesspermit) VALUES (?, ?,?,?,?,?,?)";
                PreparedStatement statement = connection.prepareStatement(query);
                
                statement.setString(1, txtPhoneNumber.getText());
                statement.setString(2, txtFirstName.getText());
                statement.setString(3, txtMiddleName.getText());
                statement.setString(4, txtLastName.getText());
                statement.setString(5, txtBusinessName.getText());
                statement.setString(6, txtBusinessAddress.getText());
                statement.setString(7, txtBusinessPermit.getText());
                
                int rowsInserted = statement.executeUpdate();
                if (rowsInserted > 0) {
                    JOptionPane.showMessageDialog(null, "Thank You for Applying! Weâ€™ll get back to you shortly");
                    this.dispose();
                    SettingsPage settingsPage = new SettingsPage();
                    settingsPage.SetAccount(phoneNumber, firstname, surname, password);
                    settingsPage.setVisible(true);
                    settingsPage.DisplayAccountInformation();
                } else {
                    JOptionPane.showMessageDialog(null, "Failed to sign up");
                }
                
                statement.close();
            }
    } catch (ClassNotFoundException | SQLException e) {
    }
    }//GEN-LAST:event_btnSignUpMerchantActionPerformed

    private void txtPhoneNumberKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPhoneNumberKeyReleased
        // TODO add your handling code here:
        String currentText = txtPhoneNumber.getText();

        String filteredText = currentText.replaceAll("[^0-9]", "");

        if (filteredText.length() > 11) {
            filteredText = filteredText.substring(0, 11);
        }

        if (!filteredText.startsWith("09")) {
            filteredText = "09" + filteredText;
        }

        txtPhoneNumber.setText(filteredText);
    }//GEN-LAST:event_txtPhoneNumberKeyReleased

